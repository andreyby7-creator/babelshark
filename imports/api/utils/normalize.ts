/**
 * @file Нормализация входных строк для детерминированного поиска.
 *
 * @remarks
 * - Держим нормализацию как SSOT, чтобы не дублировать `trim + lowerCase` по проекту.
 * - Runtime-проверка типов выполняется на входе (в `Meteor.method translatePosition`).
 * - Если нормализация усложнится (accent folding, пунктуация и т.п.) — вынесем в pipeline/strategy.
 */

/* ============================================================================
 * NORMALIZE
 * ========================================================================== */

/**
 * Нормализует строку для поиска в таблице `translations`.
 *
 * @param s - исходная строка
 * @returns нормализованная строка (`trim + lowerCase`)
 *
 * Решение:
 * - `trim()` убирает случайные пробелы из UI/DOM
 * - `toLowerCase()` обеспечивает стабильный ключ (SSOT для lookup/cache)
 */
export const normalizeSource = (s: string): string => s.trim().toLowerCase();
